# Ввод числа
X = int(input("Введите натуральное число: "))

# Подсчет делителей
count = 0

# Оптимизированный алгоритм - перебираем до корня из X
for i in range(1, int(X**0.5) + 1):
    if X % i == 0:
        count += 1
        if i != X // i:  # Если это не квадратный корень
            count += 1

# Вывод результата
print(f"Количество натуральных делителей числа {X}: {count}")