# Создаем пустой словарь для питомцев
pets = {}

# Ввод количества питомцев
num_pets = int(input("Сколько питомцев вы хотите добавить? "))

# Заполняем словарь данными
for i in range(num_pets):
    print(f"\n--- Ввод данных для питомца {i + 1} ---")

    # Ввод данных
    pet_name = input("Введите имя питомца: ")
    pet_type = input("Введите вид питомца: ")
    pet_age = int(input("Введите возраст питомца: "))
    owner_name = input("Введите имя владельца: ")

    # Добавляем в словарь
    pets[pet_name] = {
        'Вид питомца': pet_type,
        'Возраст питомца': pet_age,
        'Имя владельца': owner_name
    }

# Выводим получившийся словарь
print("\n--- Получившийся словарь pets ---")
print(pets)

# Красивый вывод информации о питомцах
print("\n--- Информация о питомцах ---")
for pet_name, pet_info in pets.items():
    print(f"\nПитомец: {pet_name}")
    print(f"  Вид: {pet_info['Вид питомца']}")
    print(f"  Возраст: {pet_info['Возраст питомца']} года/лет")
    print(f"  Владелец: {pet_info['Имя владельца']}")