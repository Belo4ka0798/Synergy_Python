# Ввод N с проверкой
N = int(input("Введите целое число: "))
if not (1 < N < 100000):
    print("Ошибка: N должно быть в диапазоне 1 < N < 100000")
    exit()

# Ввод массива
arr = list(map(int, input().split()))

# Проверка количества элементов
if len(arr) != N:
    print("Ошибка: количество чисел не соответствует N")
    exit()

# Проверка диапазона чисел
for num in arr:
    if not (1 < num < 10**9):
        print(f"Ошибка: число {num} вне допустимого диапазона")
        exit()

# Циклический сдвиг и вывод
shifted = [arr[-1]] + arr[:-1]
print(" ".join(map(str, shifted)))